// 构建 Set 便于快速查找
const _holidaySet = new Set()
const _workdaySet = new Set()
Object.values(holidays).forEach(arr => arr.forEach(d => _holidaySet.add(d)))
Object.values(workdays).forEach(arr => arr.forEach(d => _workdaySet.add(d)))

/**
 * 获取日期类型
 * @param {string} dateStr 格式 YYYY-MM-DD
 * @returns {'workday'|'holiday'|'restday'}
 */
function getDayType(dateStr) {
  if (_holidaySet.has(dateStr)) return 'holiday'
  if (_workdaySet.has(dateStr)) return 'workday'
  const [y, m, d] = dateStr.split('-').map(Number)
  const day = new Date(y, m - 1, d).getDay()
  return (day === 0 || day === 6) ? 'restday' : 'workday'
}

/**
 * 是否为休息日（假日或周末）
 * @param {string} dateStr 格式 YYYY-MM-DD
 * @returns {boolean}
 */
function isRestDay(dateStr) {
  const type = getDayType(dateStr)
  return type === 'holiday' || type === 'restday'
}

